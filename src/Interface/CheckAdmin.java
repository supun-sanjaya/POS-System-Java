    public void UpdateUsersDetails()
    {
        UpdateForm up=new  UpdateForm();
        
       String nic = up.txt_nic.getText();
        String address = up.txt_adress4.getText();
        String name = up.txt_name.getText();
        String tp = up.txt_tp.getText();
        String password = up.txt_password.getText();
        String username = up.txt_username.getText();
        String position=up.txt_position.getText();
        try {

            Statement s = myconnection.getConnection().createStatement();
            s.executeUpdate(" UPDATE `users` SET NIC ='"+nic+"'"
                + " ,Address ='"+address+"'"
                + ",Name ='"+name+"'"
                + ",TP ='"+tp+"' "
                + ",Password ='"+password+"' "
                + ",Position ='"+position+"' "
                + " WHERE Username = '"+username+"' ");
            JOptionPane.showMessageDialog(null, "Dtata Updated");

        } catch (HeadlessException | SQLException e) {
            System.out.println(e);
        }
    
    }
