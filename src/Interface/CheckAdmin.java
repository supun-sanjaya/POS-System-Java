public void invoiceid()
    {
        try {
           
            Statement s = myconnection.createStatement();
            ResultSet rs = s.executeQuery("SELECT MAX(`Bill_no`) FROM `sales`");
            rs.next();
            rs.getString("MAX(`Bill_no`)");
            if (rs.getString("MAX(`Bill_no`)") == null) {
                txt_qtyform.setText("000001");     
            } else {
                long id = Long.parseLong(rs.getString("MAX(`Bill_no`)").substring(2, rs.getString("MAX(`Bill_no`)").length()));
                id++;
                txt_qtyform.setText(String.format("%06d", id));
            }
        } catch (SQLException | NumberFormatException ex) {
            ex.printStackTrace();
        }      
    }
